          </label>
        </div>

        <Transition name="fade" mode="out-in">
          <div v-if="localCustom" key="custom" class="rounds-body">
            <div class="rounds-display">
              <span class="rounds-num">{{ localMaxRounds }}</span>
              <span class="rounds-unit">{{ $t('step2.roundsUnit') }}</span>
              <span class="rounds-est">≈ {{ Math.round(localMaxRounds * 0.6) }}min</span>
            </div>
            <div class="range-wrap">
              <input
                type="range"
                v-model.number="localMaxRounds"
                min="10"
                :max="autoGeneratedRounds"
                step="5"
                class="range-input"
                :style="{ '--fill': fillPct }"
              />
              <div class="range-marks">
                <span>10</span>
                <span
                  class="mark-rec"
                  :class="{ active: localMaxRounds === 40 }"
                  @click="localMaxRounds = 40"
                >{{ $t('step2.recommendedRounds', { rounds: 40 }) }}</span>
                <span>{{ autoGeneratedRounds }}</span>
              </div>
            </div>
          </div>

          <div v-else key="auto" class="rounds-auto">
            <div class="auto-num">
              <span class="rounds-num">{{ autoGeneratedRounds }}</span>
              <span class="rounds-unit">{{ $t('step2.roundsUnit') }}</span>
            </div>
            <p class="auto-tip" @click="localCustom = true">
              {{ $t('step2.customTip') }} →
            </p>
          </div>
        </Transition>
      </div>

      <!-- Ações -->
      <div class="action-row">
        <button class="btn-secondary" @click="$emit('go-back')">
          ← {{ $t('step2.backToGraphBuild') }}
        </button>
        <button
          class="btn-primary"
          :disabled="phase < 4"
          @click="handleStart"
        >
          {{ $t('step2.startDualWorldSim') }} →
        </button>
      </div>
    </div>
  </div>
</template>

<script setup>
import { ref, computed, watch } from 'vue'

const props = defineProps({
  phase:              { type: Number,  required: true },
  simulationConfig:   { type: Object,  default: null },
  autoGeneratedRounds:{ type: Number,  default: null },
  useCustomRounds:    { type: Boolean, default: false },
  customMaxRounds:    { type: Number,  default: 40 },
})

const emit = defineEmits(['go-back', 'start-simulation', 'update:useCustomRounds', 'update:customMaxRounds'])

const localCustom    = ref(props.useCustomRounds)
const localMaxRounds = ref(props.customMaxRounds)

// Sync child → parent
watch(localCustom,    (v) => emit('update:useCustomRounds', v))
watch(localMaxRounds, (v) => emit('update:customMaxRounds', v))

// Sync parent → child (caso o pai mude externamente, ex: remount)
watch(() => props.useCustomRounds, (v) => { if (v !== localCustom.value) localCustom.value = v })
watch(() => props.customMaxRounds, (v) => { if (v !== localMaxRounds.value) localMaxRounds.value = v })

// Guard: quando autoGeneratedRounds fica disponível ou muda,
// clamp localMaxRounds para não exceder o máximo do slider.
watch(() => props.autoGeneratedRounds, (max) => {
  if (max !== null && localMaxRounds.value > max) {
    localMaxRounds.value = max
  }
})

const fillPct = computed(() => {
  if (!props.autoGeneratedRounds) return '0%'
  return ((localMaxRounds.value - 10) / (props.autoGeneratedRounds - 10)) * 100 + '%'
})

const handleStart = () => {
  const params = {}
  if (localCustom.value) params.maxRounds = localMaxRounds.value
  emit('start-simulation', params)
}
</script>

