<template>
  <div class="setup-card" :class="{ 'card--active': phase >= 4, 'card--pending': phase < 4 }">
    <div class="card-head">
      <div class="card-label">
        <span class="card-num">05</span>
        <span class="card-title">{{ $t('step2.setupComplete') }}</span>
      </div>
      <span class="card-badge" :class="phase >= 4 ? 'badge--active' : 'badge--pending'">
        {{ phase >= 4 ? $t('step1.inProgress') : $t('common.pending') }}
      </span>
    </div>

    <div class="card-body">
      <p class="api-route">POST /api/simulation/start</p>
      <p class="card-desc">{{ $t('step2.setupCompleteDesc') }}</p>

      <!-- Configuração de rounds — só aparece quando ready -->
      <div v-if="simulationConfig && autoGeneratedRounds" class="rounds-section">
        <div class="rounds-head">
          <div class="rounds-info">
            <span class="rounds-title">{{ $t('step2.roundsConfig') }}</span>
            <span class="rounds-desc">
              {{ $t('step2.roundsConfigDesc', {
                hours: simulationConfig?.time_config?.total_simulation_hours || '—',
                minutesPerRound: simulationConfig?.time_config?.minutes_per_round || '—'
